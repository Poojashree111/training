package training;

public class Unary {

	public static void main(String[] args) {
		int n=+60;
		System.out.println(n);
		n=-n;
		System.out.println(n);
		int k=40;
		k++;
		System.out.println(k);
		k--;
		System.out.println(k);
		boolean flag=false;
		System.out.println(flag);
		System.out.println(!flag);

		
	}

}
